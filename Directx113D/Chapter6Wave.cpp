Chapter6Wave::Chapter6Wave() 
	: mLandVB(0), mLandIB(0), mWavesVB(0), mWavesIB(0), mFX(0), mTech(0),
	mfxWorldViewProj(0), mInputLayout(0), mWireframeRS(0), mGridIndexCount(0),
	mTheta(1.5f*XM_PI), mPhi(0.25f*XM_PI), mRadius(5.0f),
	px(0), py(0), pz(0)
{
	mTitleCaption = L"Waves Demo";

	mLastMousePos.x = 0;
	mLastMousePos.y = 0;

	XMMATRIX I = XMMatrixIdentity();
	XMStoreFloat4x4(&mGridWorld, I);
	XMStoreFloat4x4(&mWavesWorld, I);
	XMStoreFloat4x4(&mView, I);
	XMStoreFloat4x4(&mProj, I);

}

